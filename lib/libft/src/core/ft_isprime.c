#include "libft.h"

int	ft_isprime(size_t c)
{
	size_t	i;

	if (c <= 3)
		return (c > 1);
	if (c % 2 == 0 || c % 3 == 0)
		return (0);
	i = 5;
	while (i <= c / i)
	{
		if (c % i == 0 || c % (i + 2) == 0)
			return (0);
		i += 6;
	}
	return (1);
}
