#include "cub3d.h"

uint32_t	apply_wall_shading(uint32_t rgba)
{
	t_rgba	c;

	c.r = (((rgba >> 24) & 0xFF) * 3) >> 2;
	c.g = (((rgba >> 16) & 0xFF) * 3) >> 2;
	c.b = (((rgba >> 8) & 0xFF) * 3) >> 2;
	c.a = ((rgba >> 0) & 0xFF);
	return ((c.r << 24) | (c.g << 16) | (c.b << 8) | c.a);
}
